def add(first_num: int, second_num: int) -> int:
    """Add two numbers.

    Note:
        This is only for demonstration purposes and it will be removed later.

    Examples:
        >>> add(1, 2)
        3
        >>> add(5, 3)
        8

    Args:
        first_num: The first number to be added.
        second_num: The second number to be added.

    Returns:
        The calculated sum of the two numbers.
    """
    return first_num + second_num
